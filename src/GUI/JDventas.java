    private void configurarTabla() {
        String[] columnas = {
            "ID", "N° Comprobante", "Tipo", "Cliente",
            "Método Pago", "Cajero", "Fecha", "Total S/"
        };

        modeloTabla = new DefaultTableModel(null, columnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // solo lectura
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                if (columnIndex == 7) {
                    return Double.class;
                }
                return String.class;
            }
        };
        tblVentas.setModel(modeloTabla);
        tblVentas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        // Ocultar columna ID (índice 0)
        tblVentas.getColumnModel().getColumn(0).setMinWidth(0);
        tblVentas.getColumnModel().getColumn(0).setMaxWidth(0);
        tblVentas.getColumnModel().getColumn(0).setPreferredWidth(0);

        // Ancho columnas visibles
        tblVentas.getColumnModel().getColumn(1).setPreferredWidth(120);
        tblVentas.getColumnModel().getColumn(2).setPreferredWidth(90);
        tblVentas.getColumnModel().getColumn(3).setPreferredWidth(180);
        tblVentas.getColumnModel().getColumn(4).setPreferredWidth(110);
        tblVentas.getColumnModel().getColumn(5).setPreferredWidth(100);
        tblVentas.getColumnModel().getColumn(6).setPreferredWidth(140);
        tblVentas.getColumnModel().getColumn(7).setPreferredWidth(80);
    }

    private void cargarVentas(String filtro) {
        modeloTabla.setRowCount(0);
        VentaDAO dao = new VentaDAO();
        List<Object[]> lista = dao.listarTodasLasVentas();

        for (Object[] fila : lista) {
            // fila[3] = cliente, fila[1] = comprobante — filtramos por ambos
            String comprobante = fila[1] != null ? fila[1].toString().toLowerCase() : "";
            String cliente = fila[3] != null ? fila[3].toString().toLowerCase() : "";
            String filtroLow = filtro.toLowerCase().trim();

            if (filtroLow.isEmpty()
                    || comprobante.contains(filtroLow)
                    || cliente.contains(filtroLow)) {
                modeloTabla.addRow(fila);
            }
        }
    }

